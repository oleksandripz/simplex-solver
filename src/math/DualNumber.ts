// експортую мій спеціальний клас для роботи з числами, що містять штучний штраф (літеру M)
export class DualNumber {
  // створюю конструктор, який зберігає реальну частину (r) та коефіцієнт біля нескінченності (m). за замовчуванням m = 0
  constructor(public r: number, public m: number = 0) {}

  // метод для додавання іншого дуального числа до поточного
  add(other: DualNumber): DualNumber {
    // повертаю нове число, де окремо додаю реальні частини та окремо коефіцієнти біля M
    return new DualNumber(this.r + other.r, this.m + other.m);
  }

  // метод для віднімання іншого дуального числа з поточного
  sub(other: DualNumber): DualNumber {
    // повертаю нове число, віднявши відповідні компоненти
    return new DualNumber(this.r - other.r, this.m - other.m);
  }

  // метод для множення мого дуального числа на звичайне число (скаляр)
  mul(scalar: number): DualNumber {
    // просто множу і реальну частину, і коефіцієнт M на цей скаляр
    return new DualNumber(this.r * scalar, this.m * scalar);
  }

  // метод для ділення мого дуального числа на звичайне число
  div(scalar: number): DualNumber {
    // перевіряю, чи скаляр не дорівнює нулю (з урахуванням похибки JS), щоб не зламати програму
    if (Math.abs(scalar) < 1e-9) throw new Error("Ділення на нуль");
    // повертаю нове число, поділивши обидві частини на скаляр
    return new DualNumber(this.r / scalar, this.m / scalar);
  }

  // метод, який перевіряє, чи є моє число від'ємним (це потрібно для пошуку напрямного стовпця при мінімізації)
  isNegative(): boolean {
    // оскільки M — це нескінченно велике число, то якщо коефіцієнт біля нього від'ємний, все число від'ємне
    if (this.m < -1e-7) return true;
    // якщо M дорівнює нулю, тоді дивлюсь на реальну частину: якщо вона від'ємна, то і число від'ємне
    if (Math.abs(this.m) <= 1e-7 && this.r < -1e-7) return true;
    // якщо жодна з умов не спрацювала, число є додатним (або нулем)
    return false;
  }

  // метод, який перевіряє, чи є число додатним (додав це для підтримки задач на максимізацію)
  isPositive(): boolean {
    // якщо коефіцієнт біля нескінченності додатний, то число 100% додатне
    if (this.m > 1e-7) return true;
    // якщо M немає, перевіряю, чи додатна реальна частина
    if (Math.abs(this.m) <= 1e-7 && this.r > 1e-7) return true;
    // інакше число від'ємне або нуль
    return false;
  }

  // метод для порівняння двох дуальних чисел (використовую для пошуку найкращої оцінки та тета-тесту)
  compareTo(other: DualNumber): number {
    // спочатку рахую різницю між коефіцієнтами біля M
    const mDiff = this.m - other.m;
    // оскільки M нескінченне, то якщо коефіцієнти відрізняються, саме вони визначають, яке число більше
    if (Math.abs(mDiff) > 1e-7) return mDiff;
    // якщо коефіцієнти біля M однакові (або їх немає), порівнюю лише реальні частини
    return this.r - other.r;
  }

  // метод, який перетворює мій математичний об'єкт у красивий текстовий рядок для таблиці
  toString(): string {
    // створюю допоміжну стрілочну функцію для форматування чисел
    const format = (n: number) => {
      // округлюю число до двох знаків після коми
      const fixed = n.toFixed(2);
      // якщо число ціле (закінчується на .00), то відрізаю ці нулі для красивого вигляду
      return fixed.endsWith('.00') ? fixed.slice(0, -3) : fixed;
    };

    // якщо коефіцієнт біля M дорівнює нулю, просто повертаю відформатовану реальну частину
    if (Math.abs(this.m) <= 1e-7) return format(this.r);

    // якщо реальна частина дорівнює нулю, потрібно вивести лише M
    if (Math.abs(this.r) <= 1e-7) {
      // якщо M дорівнює рівно 1, виводжу просто літеру 'M' без одиниці спереду
      if (Math.abs(this.m - 1) <= 1e-7) return 'M';
      // якщо M дорівнює рівно -1, виводжу просто '-M'
      if (Math.abs(this.m + 1) <= 1e-7) return '-M';
      // якщо це інший коефіцієнт, виводжу його разом із M (наприклад, 2.5M)
      return `${format(this.m)}M`;
    }

    // якщо є і реальна частина, і M, визначаю знак між ними (плюс чи нічого, бо мінус сам підставиться)
    const sign = this.m > 0 ? '+' : '';

    // формую частину з літерою M, так само перевіряючи одиниці та мінус одиниці
    const mStr = Math.abs(this.m - 1) <= 1e-7 && this.m > 0 ? 'M' :
                 Math.abs(this.m + 1) <= 1e-7 ? '-M' : `${format(this.m)}M`;

    // склеюю і повертаю готовий рядок (наприклад, "10+M" або "5-2.5M")
    return `${format(this.r)}${sign}${mStr}`;
  }
}